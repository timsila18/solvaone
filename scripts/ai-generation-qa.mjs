function parseOutput(text) {
  const cleaned = text.replace(/^```json/i, "").replace(/^```/, "").replace(/```$/g, "").trim();
  return outputSchema.parse(JSON.parse(cleaned));
}

function validateProduct(product, output) {
  const joined = `${output.executiveSummary}\n${output.sections.map((section) => `${section.title}\n${section.html}`).join("\n")}`.toLowerCase();
  const checks = [];

  checks.push(["has rich section content", output.sections.reduce((sum, section) => sum + section.html.length, 0) >= 1500]);
  checks.push(["has improvement notes", output.improvementNotes.length > 0 || output.qualityScores.notes.length > 0]);
  checks.push(["handles missing info", output.missingInformation.length > 0 || joined.includes("to be provided")]);
  checks.push(["no markdown fences", !joined.includes("```")]);
  checks.push(["no script tags", !joined.includes("<script")]);

  if (product.startsWith("cv_")) {
    checks.push(["has ATS keywords", output.atsKeywords.length >= 5]);
    checks.push(["ATS score present", typeof output.qualityScores.ats === "number"]);
  }
  if (product === "cv_revamp") {
    checks.push(["lists improvements made", output.improvementsMade.length >= 2]);
    checks.push(["contains CV sections", /summary|experience|education|skills/.test(joined)]);
  }
  if (product === "cover_letter") {
    checks.push(["letter-shaped content", /dear|sincerely|faith achieng|customer experience officer/.test(joined)]);
  }
  if (product === "company_profile") {
    checks.push(["tender readiness score present", typeof output.qualityScores.tenderReadiness === "number"]);
    checks.push(["company profile sections", /company overview|services|why choose|compliance|contact/.test(joined)]);
  }
  if (product === "business_plan") {
    checks.push(["business clarity score present", typeof output.qualityScores.businessClarity === "number"]);
    checks.push(["business plan sections", /market analysis|operations|financial|roadmap|risk/.test(joined)]);
  }

  return checks;
}

if (!process.env.OPENAI_API_KEY) {
  console.error("OPENAI_API_KEY is not configured.");
  process.exit(1);
}

const client = new OpenAI({ apiKey: process.env.OPENAI_API_KEY });
const model = process.env.OPENAI_MODEL || "gpt-4.1-mini";
const results = [];

for (const testCase of cases) {
  const started = Date.now();
  const response = await client.responses.create({
    model,
    input: [
      { role: "system", content: baseSystem },
      { role: "developer", content: `${instructions[testCase.product]}\n\n${outputContract}` },
      { role: "user", content: JSON.stringify(testCase, null, 2) }
    ],
    temperature: 0.45,
    max_output_tokens: 6000
  });

  let output;
  let parseError = null;
  try {
    output = parseOutput(response.output_text || "");
  } catch (error) {
    parseError = error;
  }

  const checks = output ? validateProduct(testCase.product, output) : [];
  const failedChecks = checks.filter(([, passed]) => !passed).map(([name]) => name);
  const sectionChars = output ? output.sections.reduce((sum, section) => sum + section.html.length, 0) : 0;

  results.push({
    product: testCase.product,
    status: parseError || failedChecks.length ? "FAIL" : "PASS",
    title: output?.title || "N/A",
    sections: output?.sections.length || 0,
    sectionChars,
    scores: output?.qualityScores || null,
    missingInfoCount: output?.missingInformation.length || 0,
    improvementsCount: output?.improvementsMade.length || 0,
    atsKeywordsCount: output?.atsKeywords.length || 0,
    failedChecks,
    error: parseError ? parseError.message : null,
    usage: response.usage || null,
    ms: Date.now() - started
  });
}

